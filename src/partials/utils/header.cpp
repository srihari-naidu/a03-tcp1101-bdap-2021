void header(string header)
{
    clearScreen();

    if (header == "WELCOME")
        cout << WELCOME;
    else if (header == "LOGIN")
        cout << LOGIN;
    else if (header == "REGISTER")
        cout << REGISTER;
    else if (header == "ADMIN")
        cout << ADMIN;
    else if (header == "B.D.A.P")
        cout << B_D_A_P;
    else if (header == "STAT. ANALYSIS")
        cout << STAT_ANALYSIS;
    else if (header == "SAVE AS")
        cout << SAVE_AS;
    else if (header == "REPORT")
        cout << REPORT;
    else if (header == "MANAGE ACCT.")
        cout << MANAGE_ACCT;
    else if (header == "CHANGE PASS.")
        cout << CHANGE_PASS;
    else if (header == "DELETE ACCT.")
        cout << DELETE_ACCT;
}




